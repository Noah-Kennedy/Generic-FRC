package org.usfirst.frc.team4786.robot;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
	//drive base motor ports
	public static final int frontLeftPort = 0; //placeholder value
	public static final int frontRightPort = 0; //placeholder value
	public static final int backLeftPort = 0; //placeholder value
	public static final int backRightPort = 0; //placeholder value
	
	//joystick ports
	public static final int arcadeDriveJoyPort = 0; //should be only joystick needed for drive base

}
